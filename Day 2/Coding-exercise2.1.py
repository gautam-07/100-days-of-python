# Data Types

# String
# Sub-scripting - Pulling an element out of a string is called sub scripting
# eg - print("hello"[0])    It will print 'H'.
print("Hello"[0])

# Integer
# In python if we want to write large numbers we can simply do that by writing something like 123_456_789 , Python interpreter will 
# automatically remove out underscores.
print(123 + 456)

# Float
3.14159

# Boolean
True
False

# Type Conversions
# We can use type() function to investigate a variable type
# We use str(), int(), float() functions to convert from one data type to another.

print(str(70) + str(100))
print(70 + float("100.5"))

